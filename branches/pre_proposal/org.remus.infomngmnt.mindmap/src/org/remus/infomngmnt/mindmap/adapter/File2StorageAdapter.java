package org.remus.infomngmnt.mindmap.adapter;

import org.eclipse.core.filesystem.EFS;
import org.eclipse.core.filesystem.IFileStore;
import org.eclipse.core.resources.IFile;
import org.eclipse.core.runtime.CoreException;
import org.eclipse.core.runtime.IAdapterFactory;

/**
 * @author Tom Seidel <tom.seidel@remus-software.org>
 */
public class File2StorageAdapter implements IAdapterFactory {

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.core.runtime.IAdapterFactory#getAdapter(java.lang.Object,
	 * java.lang.Class)
	 */
	public Object getAdapter(final Object adaptableObject, final Class adapterType) {
		try {
			if (adaptableObject instanceof IFile && adapterType == IFileStore.class) {
				return EFS.getStore(((IFile) adaptableObject).getLocationURI());
			}
		} catch (CoreException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		return null;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.core.runtime.IAdapterFactory#getAdapterList()
	 */
	public Class[] getAdapterList() {
		return new Class[] { IFileStore.class };
	}

}

package org.remus.infomngmnt.ui.handler;

import org.eclipse.core.commands.AbstractHandler;
import org.eclipse.core.commands.ExecutionEvent;
import org.eclipse.core.commands.ExecutionException;
import org.eclipse.swt.dnd.Clipboard;
import org.eclipse.swt.dnd.TextTransfer;
import org.eclipse.swt.dnd.Transfer;
import org.eclipse.swt.widgets.Display;
import org.eclipse.ui.handlers.HandlerUtil;

/**
 * @author Tom Seidel <tom.seidel@remus-software.org>
 */
public class CopyTextToClipboardHandler extends AbstractHandler {

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.core.commands.IHandler#execute(org.eclipse.core.commands.
	 * ExecutionEvent)
	 */
	public Object execute(final ExecutionEvent event) throws ExecutionException {
		String parameter = event.getParameter("text");
		Display display = HandlerUtil.getActiveShell(event).getDisplay();
		Clipboard clipboard = new Clipboard(display);
		clipboard.setContents(new Object[] { parameter }, new Transfer[] { TextTransfer
				.getInstance() });
		clipboard.dispose();
		return null;
	}

}
